""" This module parses output of img dupe process.
    selects images path from each dupe cluster.
    randomly picks images to produce visual samples for further investigation. 
    arguments: imgdupes output text file path, output folder to keep visual samples
    Ouput: copy random clusters images to output folder
Returns:
    _type_: _description_
"""

 
import os
import random
import shutil
from shutil import copyfile
import argparse

parser = argparse.ArgumentParser(description='analyse txt file (output of imgdupes) ')
parser.add_argument('filename',type=str,help='path to txt file generated by imgdupes')
parser.add_argument('clusters',type=str,help='number of random clusters to pick')
#args=parser.parse_args(['filename',10])
args = parser.parse_args()
#print(args)


'''
dfault parameters for script
'''
# pass path to duplicate txt file
duplicates_txt_file = args.filename#'./phash4_1.txt'
# numerb of random cluster to pick for evaluation
number_clusters= int(args.clusters)
# path of dolder to keep these random clusters
output_folder_name= duplicates_txt_file.replace('./','').split('.')[-2]+'_'+str(number_clusters)
output_folder = './'+output_folder_name+'_clusters/'


# Preprocessing duplciates.txt file
# remove irrelevent entries in duplciates.txt files 

def clean_dupl_txt_file(filename):
    with open(filename,'r') as f:
        lines = f.readlines()
    with open(filename,'w') as f:
        for line in lines:
            if line=='\n' or line.endswith(('.jpg\n','.png\n')):
                f.write(line)


## function to return clusters from txt file
def gen_cluster_from_dupl_txt(filename):
    """_generate list of clusters from text file generated by de-duplicate process_

    Args:
        filename (_text_): _path to text files output of de-duplicate process_

    Returns:
        clusters _list_: _list of clusters each containing relevent image paths_
    """
    with open(filename,'r') as f:
        lines = f.readlines()

    clusters=[]
    tmp=''
    for line in lines:
        tmp=tmp+line
        if line =='\n':
            tmp = tmp.replace('\n',',').split(',')
            cluster = []
            for i in tmp[:-2]:
                cluster.append(i)
            # append first entry to list
            if len(cluster)>0:
                clusters.append(cluster)
            tmp=''
    return clusters


def copy_samples_to_folder(samples,folderpath):
    """_copies sample of images to output folder path_

    Args:
        samples (_int_): _number of samples_
        folderpath (_str_): _output folder path_
    """
    # if folder does not exist
    # then create there
    
    for e,sample in enumerate(samples):
        sample_path = folderpath+'/'+str(e)+'/'
        if not os.path.exists(sample_path):
            os.makedirs(sample_path)
    # copy samples there
        for path in sample:
            file_name = str(path.replace('/','_').replace('.._',''))#.split('_')[-2:])
            print(path)
            print('file_name: ',str(file_name))
            copyfile(path,sample_path+file_name)


if __name__ == '__main__':
    """_main method of module to run evaluation process on output of dedupe process_
    """
    # first of process duplicates text file 
    # and remove all entries except images and empty lines
    clean_dupl_txt_file(duplicates_txt_file)
    # pick random clusters from txt file
    random_clusters = random.sample(gen_cluster_from_dupl_txt(duplicates_txt_file),number_clusters)
    # to avoid duplicates just remove folder
    if os.path.exists(output_folder):
        print('removing older data from: ',output_folder)
        shutil.rmtree(output_folder)
        copy_samples_to_folder(random_clusters,output_folder)
        print('copied new samples to: ',output_folder)
    else:
        copy_samples_to_folder(random_clusters,output_folder)
        


